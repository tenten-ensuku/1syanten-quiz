import { mkdir, writeFile } from "node:fs/promises";

const SPREADSHEET_ID = "18GPIv-pUCjXbmJREqEoT0o_ig18pYX2z1sIPqDwf2Z8";
const CSV_URL = `https://docs.google.com/spreadsheets/d/${SPREADSHEET_ID}/export?format=csv&gid=0`;
const OUTPUT_PATH = "src/lib/generatedExplanations.ts";
const strict = process.argv.includes("--strict");

function parseCsv(csvText) {
  const rows = [];
  let row = [];
  let cell = "";
  let inQuotes = false;

  for (let index = 0; index < csvText.length; index += 1) {
    const char = csvText[index];
    const nextChar = csvText[index + 1];

    if (char === '"' && inQuotes && nextChar === '"') {
      cell += '"';
      index += 1;
      continue;
    }

    if (char === '"') {
      inQuotes = !inQuotes;
      continue;
    }

    if (char === "," && !inQuotes) {
      row.push(cell);
      cell = "";
      continue;
    }

    if ((char === "\n" || char === "\r") && !inQuotes) {
      if (char === "\r" && nextChar === "\n") {
        index += 1;
      }
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
      continue;
    }

    cell += char;
  }

  if (cell.length > 0 || row.length > 0) {
    row.push(cell);
    rows.push(row);
  }

  return rows;
}

async function main() {
  const response = await fetch(CSV_URL);

  if (!response.ok) {
    throw new Error(`Failed to fetch spreadsheet CSV: ${response.status} ${response.statusText}`);
  }

  const csvText = await response.text();
  const rows = parseCsv(csvText);
  const explanations = {};

  for (const row of rows.slice(1)) {
    const source = row[0]?.trim();
    const explanation = row[1]?.trim();

    if (source && explanation) {
      explanations[source] = explanation;
    }
  }

  const count = Object.keys(explanations).length;
  if (strict && count !== 56) {
    throw new Error(`Expected 56 explanations, found ${count}`);
  }

  const generated = [
    "/* This file is generated by scripts/sync-explanations.mjs. */",
    "export const GENERATED_EXPLANATIONS: Record<string, string> = ",
    `${JSON.stringify(explanations, null, 2)};`,
    ""
  ].join("\n");

  await mkdir("src/lib", { recursive: true });
  await writeFile(OUTPUT_PATH, generated, "utf8");
  console.log(`Synced ${count} explanations from spreadsheet column B.`);
}

main().catch((error) => {
  console.error(error);
  process.exit(strict ? 1 : 0);
});
